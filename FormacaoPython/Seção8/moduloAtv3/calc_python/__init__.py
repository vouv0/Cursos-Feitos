'''
Módulo com funções para realizar soma e subtração de dois números escolhidos pelo usuário
'''

def soma(num1=0, num2=0):
    '''
    Função que soma dois números colocados pelo usuário.
    :param num1: primeiro número
    :param num2: segundo número
    :return: resultado da soma dos dois números
    '''
    return num1 + num2

def subtracao(num1, num2):
    '''
    Função que subtrai dois números colocados pelo usuário.
    :param num1: primeiro número
    :param num2: segundo número
    :return: resultado da soma dos dois números
    '''
    return num1 - num2
